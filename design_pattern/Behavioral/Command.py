# 행위 패턴의 세계를 아주 멋지게 탐험하고 계시네요! 이번에 만나볼 패턴은 '커맨드 패턴(Command Pattern)'입니다.

# 이 패턴은 "명령(요청) 자체를 하나의 '물건(객체)'으로 포장하는 기술"이에요. 가장 쉽게 이해하기 위해 고급 레스토랑의 '주문 시스템'을 상상해 볼까요?

# Receiver (수신자): 주방에서 실제로 요리를 만드는 '요리사'입니다.

# Command (명령 객체): 손님의 요구사항이 적힌 '주문서(종이)'입니다.

# Invoker (호출자): 주문서를 차곡차곡 모았다가 주방에 전달하는 '웨이터'입니다.

# 만약 손님이 주방에 직접 소리쳐서 요리를 시킨다면, 나중에 주문을 취소하거나 순서를 바꾸기 매우 힘들 거예요. 하지만 '주문서(Command)'라는 종이에 적어서 웨이터에게 주면, 웨이터는 주문서들을 모아두었다가 한 번에 실행할 수도 있고, 실수로 적은 주문을 취소할 수도 있겠죠?

# 이처럼 실제 일을 하는 사람(요리사)과 일을 시키는 사람(웨이터) 사이에 '주문서'를 두어 시스템을 유연하게 만드는 원리랍니다. 주석을 통해 코드를 한 줄씩 살펴볼게요!

# 💡 머리에 쏙 들어오는 핵심 요약
# 요청의 캡슐화: "요리해!"라는 행동 자체를 command라는 변수(객체)로 만들어서 저장했습니다. 행동을 물건처럼 다룰 수 있게 된 것이 가장 큰 마법입니다.

# 실행 시점 지연: 웨이터(invoker)가 주문을 받자마자(add_command) 바로 요리가 시작되는 게 아닙니다. 나중에 웨이터가 원할 때(execute_commands) 한꺼번에 모아서 실행할 수 있죠. (이를 활용해 '매크로 기능'이나 '예약 실행 기능'을 만들 수 있어요.)

# 되돌리기(Undo) 가능: 주문이 객체로 쟁반(리스트)에 담겨 있기 때문에, 만약 손님이 "아까 시킨 거 취소요!"라고 하면 웨이터는 주방에 가기 전에 리스트에서 방금 넣은 주문서만 쏙 빼버리면 됩니다. 그래서 문서 작업 프로그램의 '실행 취소(Ctrl+Z)' 기능도 바로 이 커맨드 패턴으로 만들어진답니다.

# abc 모듈은 "이 클래스는 뼈대 역할만 하니까, 상속받아서 써라!"라고 정해주는 도구입니다.
from abc import ABC, abstractmethod  

# ---------------------------------------------------------
# 1. Command (명령): 모든 '주문서'가 지켜야 할 기본 양식
# ---------------------------------------------------------
class Command(ABC):  
    # 모든 주문서에는 무조건 "실행해!(execute)"라는 버튼(기능)이 있어야 합니다.
    @abstractmethod  
    def execute(self):  
        pass  

# ---------------------------------------------------------
# 2. ConcreteCommand (구체적 명령): 내용이 적힌 '진짜 주문서'
# ---------------------------------------------------------
class ConcreteCommand(Command):  
    
    # 주문서를 작성할 때, "이 요리는 어떤 요리사(receiver)가 담당할지"를 적어둡니다.
    def __init__(self, receiver):  
        self.receiver = receiver  

    # 웨이터가 이 주문서의 '실행(execute)' 버튼을 누르면...
    def execute(self):  
        # 주문서에 적혀 있던 담당 요리사(receiver)에게 진짜 요리(action)를 하라고 지시합니다.
        self.receiver.action()  


# ---------------------------------------------------------
# 3. Receiver (수신자): 주방에서 대기 중인 '진짜 요리사'
# ---------------------------------------------------------
class Receiver:  
    def action(self):  
        # 요리사가 실제로 프라이팬을 들고 요리를 하는 등 핵심적인 진짜 작업을 수행합니다.
        print("Receiver action") # "요리사가 맛있게 요리를 지글지글 볶습니다!"


# ---------------------------------------------------------
# 4. Invoker (호출자): 주문서를 모아서 관리하는 '웨이터'
# ---------------------------------------------------------
class Invoker:  
    def __init__(self):  
        # 웨이터가 주문을 받을 '빈 쟁반(리스트)'을 하나 들고 있습니다.
        self._commands = []  

    def add_command(self, command):  
        # 손님이 주문서(command)를 주면, 쟁반(_commands)에 차곡차곡 쌓아둡니다.
        self._commands.append(command)  

    def execute_commands(self):  
        # 웨이터가 주방에 가서 쟁반에 쌓인 주문서들을 차례대로 꺼내며 실행합니다.
        for command in self._commands:  
            # 각 주문서에 적힌 요리 지시(execute)를 발동시킵니다!
            command.execute()  


# ---------------------------------------------------------
# 5. 실제 레스토랑에서 주문해보기!
# ---------------------------------------------------------

# 1. 주방에 '진짜 요리사(Receiver)'를 한 명 배치합니다.
receiver = Receiver()  

# 2. 요리사에게 전달할 '스테이크 주문서(ConcreteCommand)'를 하나 작성합니다.
command = ConcreteCommand(receiver)  

# 3. 홀을 돌아다닐 '웨이터(Invoker)'를 부릅니다.
invoker = Invoker()  

# 4. 웨이터의 쟁반에 방금 작성한 주문서를 올려둡니다. (아직 요리 시작 안 함!)
invoker.add_command(command)  

# 5. 웨이터가 주방으로 가서 쟁반에 있는 모든 주문서를 실행시킵니다.
# 결과: 주문서가 작동하면서, 연결된 요리사가 "Receiver action" 요리를 시작합니다!
invoker.execute_commands()