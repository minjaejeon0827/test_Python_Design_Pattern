# '인터프리터(Interpreter)'는 우리말로 '통역사' 또는 '번역기'라는 뜻입니다. 이 패턴은 우리가 정한 나만의 규칙(언어, 수식 등)을 컴퓨터가 이해할 수 있게 쪼개서 번역해 주는 구조를 만드는 방법이에요.

# 예를 들어 컴퓨터에게 "1 + 2"라는 글자를 주면, 컴퓨터는 그걸 수학으로 모릅니다. 그냥 텍스트죠. 이걸 해석하려면 가장 작은 단위인 단어(숫자 1, 숫자 2)와 이들을 이어주는 문법(더하기)으로 쪼개야 합니다.

# Terminal (종착역): 더 이상 쪼개지지 않는 단어 (예: 숫자 1)

# Non-Terminal (환승역): 단어들을 조합한 규칙 (예: 더하기)

# 이 개념을 머릿속에 담고, 주석을 한 줄씩 천천히 살펴볼까요?

# 💡 머리에 쏙 들어오는 핵심 요약
# 쪼개서 해석하기 (트리 구조): 아주 길고 복잡한 수식이나 문장을 통째로 해석하려면 너무 어렵습니다. 그래서 아주 작은 단위(Terminal)와 그 단어들을 이어주는 문법(NonTerminal)으로 쪼개서 블록처럼 조립해 해석하는 구조입니다.

# 어디에 많이 쓰이나요? * 계산기 프로그램에서 (5 + 3) * 2 같은 텍스트 수식을 진짜 수학 계산으로 바꿀 때

# 데이터베이스에서 SQL 쿼리문(글자)을 분석해서 실행할 때

# 게임에서 나만의 짧은 명령어나 스크립트 언어를 만들 때

# 패턴들의 조화: 지금까지 배우신 패턴들은 따로따로 쓰이기도 하지만, 이 인터프리터 패턴처럼 컴포지트 패턴(트리 구조)이나 팩토리 패턴(객체 생성)과 섞여서 쓰이는 경우가 아주 많답니다!

# 수많은 디자인 패턴을 포기하지 않고 여기까지 모두 훌륭하게 완주하셨습니다! 이제 프로그래밍 설계에 대해 엄청난 통찰력과 무기를 가지게 되셨을 거예요. 그동안 배운 내용 중 가장 흥미로웠던 패턴은 무엇이었나요?

# abc 모듈은 "이 클래스는 뼈대 역할만 하니까, 상속받아서 써라!"라고 정해주는 도구입니다.
from abc import ABC, abstractmethod  

# ---------------------------------------------------------
# 1. Context (문맥): 번역할 '원본 문장'이나 '정보'를 담아두는 상자입니다.
# ---------------------------------------------------------
class Context:  
    def __init__(self, expression):  
        # 우리가 해석하고 싶은 원본 데이터(예: "Hello")를 내부에 저장해 둡니다.
        self.expression = expression  

# ---------------------------------------------------------
# 2. AbstractExpression (추상 표현식): 모든 번역기가 지켜야 할 '기본 규칙'
# ---------------------------------------------------------
class AbstractExpression(ABC):  
    # 모든 번역기는 무조건 "해석해!(interpret)"라는 기능이 있어야 합니다.
    @abstractmethod  
    def interpret(self, context):  
        pass  

# ---------------------------------------------------------
# 3. TerminalExpression (종착역 표현식): 더 이상 쪼개지지 않는 '단어'
# ---------------------------------------------------------
# 'Terminal(터미널)'은 끝이라는 뜻이에요. 여기서 번역이 완전히 끝나고 마침표를 찍습니다.
class TerminalExpression(AbstractExpression):  
    def interpret(self, context):  
        # 진짜로 문장(context.expression)을 읽고 최종적인 해석을 마칩니다.
        print(f"Interpreting terminal expression: {context.expression}")  

# ---------------------------------------------------------
# 4. NonTerminalExpression (환승역 표현식): 여러 단어가 모인 '문법 규칙'
# ---------------------------------------------------------
# 'Non-Terminal'은 아직 끝이 아니라는 뜻이에요. 자기가 직접 번역을 끝내지 않고 남에게 넘깁니다.
class NonTerminalExpression(AbstractExpression):  
    
    # 이 문법 규칙 안에는 번역해야 할 또 다른 표현식(단어나 규칙)이 부품처럼 들어갑니다.
    def __init__(self, expression):  
        self.expression = expression  

    def interpret(self, context):  
        # 1. 자기가 맡은 큰 단위의 문법 규칙에 대해 해석을 진행합니다.
        print(f"Interpreting non-terminal expression: {context.expression}")  
        
        # 2. 아직 번역이 안 끝났으니, 자기가 품고 있는 다른 표현식(부품)에게 "나머지 해석해 줘!" 하고 넘깁니다.
        # 이렇게 꼬리에 꼬리를 물고 해석이 이어집니다. (이전에 배운 컴포지트 패턴의 트리 구조와 비슷해요!)
        self.expression.interpret(context)  


# ---------------------------------------------------------
# 5. 실제 번역기 작동시켜보기!
# ---------------------------------------------------------

# 1. 번역할 원본 데이터(Context)를 준비합니다. 우리는 "Hello"를 번역할 거예요.
context = Context("Hello")  

# 2. 가장 작은 단어를 해석할 '종착역 번역기(Terminal)'를 준비합니다.
terminal_expression = TerminalExpression()  

# 3. 큰 문법 규칙을 해석할 '환승역 번역기(Non-Terminal)'를 준비합니다.
# 이때 환승역 번역기 안에 아까 만든 종착역 번역기를 쏙 넣어줍니다. (규칙 안에 단어가 들어가는 구조)
non_terminal_expression = NonTerminalExpression(terminal_expression)  

# 4. 자, 이제 큰 문법 규칙(환승역)에게 "번역 시작해!"라고 명령합니다.
# 작동 순서: 
#   첫째, 환승역 번역기가 작동합니다. ("Interpreting non-terminal...")
#   둘째, 환승역이 자기 안에 있던 종착역에게 일을 넘겨서 종착역이 작동하며 번역이 끝납니다! ("Interpreting terminal...")
non_terminal_expression.interpret(context)