# 행위 패턴의 두 번째 시간, '옵저버 패턴(Observer Pattern)'에 오신 것을 환영합니다!

# '옵저버(Observer)'는 관찰자라는 뜻인데요. 이 패턴은 우리가 매일 사용하는 '유튜브 채널 구독 시스템'이나 '단톡방 알림'을 떠올리시면 아주 완벽하게 이해할 수 있습니다.

# Subject (주제/발행자): 영상이나 소식을 올리는 '유튜브 채널'입니다.

# Observer (관찰자/구독자): 채널을 구독하고 알림을 기다리는 '시청자'입니다.

# 만약 이 패턴이 없다면, 시청자가 매일매일 채널에 들어가서 "새 영상 올라왔나요?" 하고 계속 물어봐야(확인해야) 할 거예요. 하지만 옵저버 패턴을 쓰면, 시청자는 그냥 '구독 버튼'만 한 번 눌러두고 자기 할 일을 하면 됩니다. 채널에 새 영상이 올라오면 알아서 띠링~ 하고 '알림'을 보내주니까요!

# 유튜브 구독 시스템을 머릿속에 그리면서, 주석을 한 줄씩 천천히 따라가 볼까요?

# 💡 머리에 쏙 들어오는 핵심 요약
# 계속 물어볼 필요가 없다 (효율성): 구독자 객체가 "데이터 바뀌었나요?" 하고 1초마다 물어보는 방식(폴링 방식이라고 해요)은 컴퓨터를 아주 힘들게 합니다. 옵저버 패턴은 "바뀌면 내가 알려줄 테니까, 넌 그냥 기다리고 있어!"라는 아주 쿨하고 효율적인 방식입니다.

# 서로의 사생활 존중 (느슨한 결합): 유튜브 채널은 구독자가 밥을 먹는지 잠을 자는지 알 필요가 없습니다. 그냥 명단에 대고 '알림'만 쏘면 끝이죠. 구독자 역시 채널이 영상을 어떻게 편집하는지 알 필요 없이 알림이 오면 보기만 하면 됩니다. 이렇게 서로 너무 깊게 알지 않아도(느슨하게 연결되어도) 완벽하게 작동하는 것이 핵심입니다.

# 어디에 많이 쓰이나요? 스마트폰의 푸시 알림, 온도가 바뀌면 숫자가 바뀌는 날씨 앱, 주식 가격이 변동되면 그래프가 바뀌는 주식 앱 등, '한쪽의 데이터가 변했을 때 다른 쪽이 자동으로 반응해야 하는 모든 곳'에 필수적으로 쓰이는 아주 중요한 패턴이랍니다!

# ---------------------------------------------------------
# 1. Subject: 소식을 전하는 '유튜브 채널 (발행자)'
# ---------------------------------------------------------
class Subject:
    def __init__(self):
        # 채널을 처음 개설하면, 구독자 명단을 적을 빈 수첩(리스트)을 하나 준비합니다.
        self._observers = []

    def add_observer(self, observer):
        # 누군가 '구독 버튼'을 누르면, 이 채널의 구독자 명단에 그 사람을 추가합니다.
        self._observers.append(observer)

    def notify_observers(self):
        # 채널에 새로운 영상이 올라왔을 때 작동하는 '전체 알림 발송' 버튼입니다.
        # 구독자 명단(_observers)에 있는 모든 사람들을 한 명씩 차례대로 불러서...
        for observer in self._observers:
            # "새 영상 떴어요!" 하고 각자의 휴대폰에 알림(update)을 울리게 합니다.
            observer.update()


# ---------------------------------------------------------
# 2. Observer: 소식을 받는 '구독자'들의 기본 규칙
# ---------------------------------------------------------
class Observer:
    def update(self):
        # "구독자라면, 채널에서 알림이 왔을 때 어떻게든 반응(update)해야 한다"는 규칙입니다.
        pass


# ---------------------------------------------------------
# 3. ConcreteObserver: 실제 유튜브를 보는 '찐 구독자'
# ---------------------------------------------------------
class ConcreteObserver(Observer):
    def update(self):
        # 휴대폰에 알림(update)이 오면 실제로 하는 행동을 적어줍니다.
        print("Observer notified") # "오, 알림 왔다! 새 영상 보러 가야지!"


# ---------------------------------------------------------
# 4. 실제 유튜브 채널 개설하고 구독해보기!
# ---------------------------------------------------------

# 1. 새로운 유튜브 채널(Subject)을 하나 개설합니다. (현재 구독자 0명)
subject = Subject()

# 2. 유튜브를 시청하는 찐 구독자(Observer)가 한 명 등장합니다.
observer = ConcreteObserver()

# 3. 시청자가 채널이 마음에 들어서 '구독(add_observer)' 버튼을 누릅니다!
# 이제 명단에 이 구독자가 등록되었습니다.
subject.add_observer(observer)

# 4. 채널 주인이 밤을 새워 편집한 '새로운 영상'을 업로드하고, 전체 알림(notify)을 발송합니다!
# 결과: 명단에 있던 구독자의 휴대폰에서 "Observer notified" 하고 알림이 울립니다.
subject.notify_observers()