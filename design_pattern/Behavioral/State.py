# 이 패턴을 가장 쉽게 이해하려면 우리가 매일 쓰는 스마트폰의 '다크 모드 / 라이트 모드 전환 버튼'을 떠올려 보시면 됩니다.

# 스마트폰 화면이 밝은 '라이트 모드(상태 A)'일 때 버튼을 누르면 어두운 '다크 모드(상태 B)'로 바뀝니다. 그리고 다시 똑같은 버튼을 누르면 원래의 '라이트 모드(상태 A)'로 돌아오죠.

# 이처럼 '나는 똑같은 버튼을 눌렀을 뿐인데, 현재 스마트폰이 어떤 상태인지에 따라 알아서 다르게 행동하고 다음 상태로 넘어가는 마법'이 바로 상태 패턴입니다.

# 어떻게 이런 일이 가능한지 스마트폰 화면 모드의 비유를 떠올리며 주석을 한 줄씩 천천히 살펴볼까요?

# 💡 머리에 쏙 들어오는 핵심 요약
# 스스로 변신하는 객체: 이 패턴의 가장 큰 매력은 상태 객체 스스로가 자기가 할 일을 끝낸 뒤, "다음은 네 차례야!" 하고 다음 상태로 전환(바통 터치)까지 책임진다는 점입니다.

# if-else가 사라지는 마법: 만약 이 패턴을 안 썼다면, 스마트폰 본체 안에 if 현재 상태 == 'A': A행동해라; 다음은 B로 바꿔라; elif 현재 상태 == 'B': ... 처럼 아주 길고 복잡한 조건문이 들어갔을 거예요. 상태 패턴은 이런 복잡한 조건문을 없애고 코드를 아주 깔끔하게 만들어줍니다.

# 전략 패턴(Strategy)과의 차이점: 전략 패턴이 로봇의 무기를 주인이 밖에서 '수동으로' 찰칵 갈아 끼워주는 것이라면, 상태 패턴은 로봇이 자기 기분이나 체력에 맞춰서 '스스로(자동으로)' 모드를 계속 바꾸며 움직이는 것에 가깝습니다!


# abc 모듈은 "이 클래스는 뼈대 역할만 하니까, 상속받아서 써라!"라고 정해주는 도구입니다.
from abc import ABC, abstractmethod  

# ---------------------------------------------------------
# 1. State (상태): 모든 상태가 지켜야 할 '기본 규칙'
# ---------------------------------------------------------
class State(ABC):  
    # 모든 상태(모드)는 버튼이 눌렸을 때 '행동(handle)'하는 기능이 무조건 있어야 합니다.
    @abstractmethod  
    def handle(self, context):  
        pass  

# ---------------------------------------------------------
# 2. ConcreteState A, B (구체적인 진짜 상태들)
# ---------------------------------------------------------

# '라이트 모드(밝은 화면)' 상태라고 상상해 보세요.
class ConcreteStateA(State):  
    def handle(self, context):  
        # 1. 라이트 모드일 때 해야 할 일을 합니다.
        print("State A") # "현재 라이트 모드입니다! 화면을 밝게 만듭니다."
        
        # 2. 할 일을 다 마친 뒤, 스마트폰(context)의 상태를 '다크 모드(B)'로 바꿔버립니다!
        # (다음번 버튼이 눌릴 때를 대비해서 미리 바통을 터치해 두는 거예요)
        context.state = ConcreteStateB()  

# '다크 모드(어두운 화면)' 상태라고 상상해 보세요.
class ConcreteStateB(State):  
    def handle(self, context):  
        # 1. 다크 모드일 때 해야 할 일을 합니다.
        print("State B") # "현재 다크 모드입니다! 화면을 어둡게 만듭니다."
        
        # 2. 할 일을 다 마친 뒤, 스마트폰(context)의 상태를 다시 '라이트 모드(A)'로 바꿔버립니다!
        context.state = ConcreteStateA()  


# ---------------------------------------------------------
# 3. Context (컨텍스트): 상태를 가지고 있는 '스마트폰 본체'
# ---------------------------------------------------------
class Context:  
    # 스마트폰을 처음 샀을 때(생성할 때), 기본 상태를 설정해 줍니다.
    def __init__(self, state):  
        # 스마트폰 내부에 현재 상태를 기억해 둡니다.
        self.state = state  

    # 사용자가 '화면 모드 변경 버튼'을 눌렀을 때 실행되는 함수입니다.
    def request(self):  
        # 스마트폰 본체는 직접 고민하지 않고, 
        # "현재 상태야, 버튼 눌렸으니까 네가 알아서 행동해!"라며 현재 상태에게 일을 넘겨버립니다.
        self.state.handle(self)  


# ---------------------------------------------------------
# 4. 실제 스마트폰 모드 변경 버튼 눌러보기!
# ---------------------------------------------------------

# 1. 스마트폰(Context)을 처음 켜면서, 기본 상태를 '상태 A(라이트 모드)'로 맞춰둡니다.
context = Context(ConcreteStateA())  

# 2. 사용자가 버튼을 한 번 누릅니다! (request)
# 결과: 현재 상태 A가 작동해서 "State A"를 출력하고, 스스로 상태 B로 변신합니다.
context.request()  # State A  

# 3. 사용자가 버튼을 한 번 더 누릅니다! (request)
# 결과: 아까 상태 B로 변신해 두었기 때문에, 이번엔 상태 B가 작동해서 "State B"를 출력하고, 다시 상태 A로 돌아갑니다.
context.request()  # State B