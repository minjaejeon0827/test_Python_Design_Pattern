# 동적(Dynamic) 변경: 프로그램이 실행되고 있는 도중(set_strategy 사용)에도, 사용자의 선택이나 상황에 맞춰서 알고리즘(싸우는 방식)을 자유자재로 바꿀 수 있습니다.

# if-else 지옥 탈출: 만약 이 패턴을 쓰지 않았다면, 로봇 안에 if 상태 == '더하기': 더한다, elif 상태 == '빼기': 뺀다 식으로 끝도 없는 조건문이 달렸을 거예요. 전략 패턴은 이런 복잡한 조건문을 깔끔하게 독립된 부품으로 나누어 줍니다.

# ---------------------------------------------------------
# 1. Strategy (전략): 모든 무기가 지켜야 할 '기본 규칙'
# ---------------------------------------------------------
class Strategy:
    def execute(self, a, b):
        # "모든 전략(무기)은 숫자 두 개를 받아서 실행(execute)하는 기능이 있어야 한다"는 규칙입니다.
        pass

# ---------------------------------------------------------
# 2. ConcreteStrategy (구체적인 전략): 실제 무기 부품들
# ---------------------------------------------------------

# 첫 번째 무기: '더하기' 전담 무기입니다.
class ConcreteStrategyAdd(Strategy):
    def execute(self, a, b):
        # 이 무기를 사용하면 두 숫자를 더해줍니다.
        return a + b

# 두 번째 무기: '빼기' 전담 무기입니다.
class ConcreteStrategySubtract(Strategy):
    def execute(self, a, b):
        # 이 무기를 사용하면 앞 숫자에서 뒤 숫자를 빼줍니다.
        return a - b

# ---------------------------------------------------------
# 3. Context (컨텍스트): 무기를 장착하고 사용하는 '로봇'
# ---------------------------------------------------------
class Context:
    
    # 처음에 로봇을 생산할 때, 기본으로 장착할 무기(전략)를 하나 쥐여줍니다.
    def __init__(self, strategy):
        # 로봇 내부에 현재 장착된 무기를 기억해 둡니다.
        self._strategy = strategy

    # set_strategy: 이 패턴의 핵심 버튼! 작동 중에도 무기를 언제든 '교체'할 수 있습니다.
    def set_strategy(self, strategy):
        # 로봇이 손에 들고 있는 무기를 새로운 무기로 바꿔 낍니다.
        self._strategy = strategy

    # 장착된 무기를 실제로 사용해 보는 버튼입니다.
    def execute_strategy(self, a, b):
        # 현재 장착된 무기(_strategy)의 기능(execute)을 실행해서 결과를 돌려줍니다.
        return self._strategy.execute(a, b)


# ---------------------------------------------------------
# 4. 실제 로봇에 무기 장착해보기!
# ---------------------------------------------------------

# 1. 로봇(Context)을 소환하면서, 손에 '더하기 무기(ConcreteStrategyAdd)'를 쥐여줍니다.
context = Context(ConcreteStrategyAdd())

# 2. 로봇에게 5와 3을 주고 무기를 작동시킵니다.
# 결과: 현재 '더하기 무기'를 들고 있으므로 5 + 3 인 8이 나옵니다.
print(context.execute_strategy(5, 3))  # 8

# 3. 이번에는 로봇 자체를 수정하지 않고, 손에 든 무기만 '빼기 무기'로 스윽 교체(set)해 줍니다.
context.set_strategy(ConcreteStrategySubtract())

# 4. 똑같이 5와 3을 주고 다시 무기를 작동시킵니다.
# 결과: 이제 '빼기 무기'를 들고 있으므로 5 - 3 인 2가 나옵니다.
print(context.execute_strategy(5, 3))  # 2