# 이 패턴은 이름 그대로 '템플릿(미리 만들어진 틀)'을 사용하는 방법이에요. 일상생활에서 흔히 볼 수 있는 '빈칸 뚫린 요리 레시피'나 '이력서 양식'을 떠올려 보시면 이해하기 아주 쉽습니다.

# 예를 들어, 프랜차이즈 식당의 본사에서 "우리 가게의 모든 요리는 무조건 1) 재료를 준비하고, 2) 불에 익힌다는 순서를 지켜야 해!"라고 전체적인 큰 틀(순서)을 정해줍니다. 하지만 각 지점마다 "어떤 재료를 준비할지", "어떻게 익힐지" 세부적인 내용은 알아서 채워 넣도록 빈칸으로 남겨두는 거죠.

# 전체적인 뼈대(알고리즘의 구조)는 부모가 미리 세워두고, 구체적인 알맹이(세부 구현)는 자식이 채워 넣는 방식! 주석을 한 줄씩 따라가며 레시피의 빈칸을 채워볼까요?

# 💡 머리에 쏙 들어오는 핵심 요약
# 코드의 중복 방지: 여러 가지 요리법을 만들 때, 매번 "1번 하고 2번 해라"라는 전체 흐름을 일일이 짜려면 귀찮겠죠? 템플릿 메소드 패턴을 쓰면 전체적인 뼈대 코드는 한 번만 작성해 두고, 달라지는 부분만 쏙쏙 골라서 만들면 되니까 코드가 훨씬 깔끔해집니다.

# 순서 통제 (할리우드 원칙): 부모 클래스(본사)가 "우리가 부를 때까지 기다려!(Don't call us, we'll call you)"라며 흐름을 꽉 잡고 있습니다. 자식 클래스는 순서를 뒤집을 권한은 없고, 그저 부모가 부를 때 자기가 맡은 빈칸(작업)만 열심히 수행하면 됩니다.

# 전략 패턴(Strategy)과의 차이:

# 이전에 배운 전략 패턴은 로봇의 무기(부품)를 통째로 갈아 끼우는 방식이었다면,

# 템플릿 메소드 패턴은 전체적인 흐름은 가만히 놔두고 그 안의 '일부분'만 자식 클래스에서 수정하여 쓰는 방식이라는 점이 다릅니다!

# abc 모듈은 "이 클래스는 뼈대 역할만 하니까, 빈칸은 상속받아서 채워라!"라고 정해주는 도구입니다.
from abc import ABC, abstractmethod

# ---------------------------------------------------------
# 1. AbstractClass (본사에서 내려준 '빈칸 뚫린 기본 레시피 틀')
# ---------------------------------------------------------
class AbstractClass(ABC):
    
    # template_method: 요리의 전체 '순서'를 강력하게 통제하는 감독관입니다.
    def template_method(self):
        # "누가 요리하든 무조건 1번 작업(operation1)부터 하고, 
        # 그 다음에 2번 작업(operation2)을 해라!"라고 순서를 못 박아둡니다.
        self.operation1()
        self.operation2()

    # 첫 번째 작업 (빈칸): 순서는 정해졌지만, 구체적으로 뭘 할지는 아직 비워둡니다.
    @abstractmethod
    def operation1(self):
        pass

    # 두 번째 작업 (빈칸): 이것도 구체적으로 뭘 할지는 지점장(자식 클래스)에게 맡깁니다.
    @abstractmethod
    def operation2(self):
        pass


# ---------------------------------------------------------
# 2. ConcreteClass (지점에서 빈칸을 채워 완성한 '진짜 요리법')
# ---------------------------------------------------------
class ConcreteClass(AbstractClass):
    
    # 본사에서 비워둔 1번 작업의 빈칸을 나만의 방식으로 채웁니다.
    def operation1(self):
        print("Operation 1") # 예: "신선한 양파와 감자를 썹니다."

    # 본사에서 비워둔 2번 작업의 빈칸도 나만의 방식으로 채웁니다.
    def operation2(self):
        print("Operation 2") # 예: "기름을 두르고 프라이팬에 볶습니다."


# ---------------------------------------------------------
# 3. 실제 레시피대로 요리해보기!
# ---------------------------------------------------------

# 1. 지점장이 완성한 나만의 요리법(ConcreteClass)을 준비합니다.
concrete = ConcreteClass()

# 2. 요리 시작! (본사에서 정해둔 전체 순서 버튼인 template_method를 누릅니다)
# 결과: "Operation 1"이 먼저 실행되고, 이어서 "Operation 2"가 알아서 차례대로 실행됩니다!
concrete.template_method()