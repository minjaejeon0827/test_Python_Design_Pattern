# 빌더 패턴 (Builder Pattern)
# 빌더 패턴은 객체 생성 과정을 단계별로 나누고,
# 각 단계별로 객체를 생성하는 방법을 지정하여,
# 객체의 생성 과정을 유연하게 관리하는 패턴입니다.
# 참고: https://wikidocs.net/252293

# 파이썬 디자인패턴 소스파일 실행 터미널 명령어
