# ---------------------------------------------------------
# 1. Product (최종 결과물): 우리가 만들 완성된 '로봇'입니다.
# ---------------------------------------------------------
class Product:
    def __init__(self):
        # 로봇의 부품들을 차곡차곡 담을 빈 상자(리스트)를 하나 준비합니다.
        self.parts = []

    def add(self, part):
        # 상자에 새로운 부품을 하나씩 추가하는 기능이에요.
        self.parts.append(part)

    def show(self):
        # 완성된 로봇에 어떤 부품들이 조립되었는지 짜잔! 하고 보여줍니다.
        print("Product parts:", ", ".join(self.parts))


# ---------------------------------------------------------
# 2. Builder (작업자): 로봇 부품을 실제로 조립하는 역할입니다.
# ---------------------------------------------------------
class Builder:
    def __init__(self):
        # 작업자가 출근해서 일을 시작할 때, 조립을 시작할 새 로봇(빈 상자)을 하나 가져옵니다.
        self.product = Product()

    def build_part_a(self):
        # 로봇에 'A 부품(예: 로봇 머리)'을 조립합니다.
        self.product.add("Part A")

    def build_part_b(self):
        # 로봇에 'B 부품(예: 로봇 몸통)'을 조립합니다.
        self.product.add("Part B")

    def get_result(self):
        # 조립이 다 끝난 로봇을 넘겨줍니다.
        return self.product


# ---------------------------------------------------------
# 3. Director (감독관): 작업자에게 조립 '순서'를 지시합니다.
# ---------------------------------------------------------
class Director:
    def __init__(self, builder):
        # 감독관은 지시를 내릴 '작업자(builder)'를 한 명 배정받습니다.
        self.builder = builder

    def construct(self):
        # 감독관이 배정받은 작업자에게 순서대로 지시를 내립니다.
        # "먼저 A 부품 조립하고, 그다음 B 부품 조립해!"
        self.builder.build_part_a()
        self.builder.build_part_b()


# ---------------------------------------------------------
# 4. 실제 조립 실행 (우리가 공장에 로봇을 주문하는 과정)
# ---------------------------------------------------------

# 1. 조립을 담당할 '작업자'를 한 명 부릅니다.
builder = Builder()

# 2. 조립 순서를 관리할 '감독관'을 부르고, 이 감독관에게 아까 부른 작업자를 연결해 줍니다.
director = Director(builder)

# 3. 감독관에게 "로봇 조립 시작해 주세요!"라고 명령합니다. 
# (그러면 감독관이 작업자에게 A 조립해, B 조립해 지시를 내립니다.)
director.construct()

# 4. 조립이 모두 끝난 후, 작업자에게 가서 "완성된 로봇 주세요!" 하고 받아옵니다.
product = builder.get_result()

# 5. 로봇이 잘 만들어졌는지 부품 목록을 확인해 봅니다.
product.show()  # 결과: Product parts: Part A, Part B

# 💡 중학생도 이해하는 핵심 포인트 설명
# 왜 이렇게 역할을 복잡하게 나눌까요?
# 그냥 처음부터 로봇 = Product() 만들고 부품을 바로 끼워 넣으면 될 텐데, 왜 굳이 작업자(Builder)와 감독관(Director)을 따로 만들었을까요?

# 바로 **"다양한 종류의 로봇을 쉽게 만들기 위해서"**입니다!

# 만약 **'날개 달린 로봇'**을 만들고 싶어지면 어떻게 할까요?

# 감독관(Director)은 그대로 두고, 날개를 조립할 줄 아는 **새로운 작업자(NewBuilder)**만 한 명 더 고용하면 됩니다.

# 감독관은 똑같이 "조립 시작해!"라고 명령하지만, 새로운 작업자가 조립하기 때문에 결과물은 '날개 달린 로봇'이 나오는 것이죠.

# 즉, **만드는 순서(감독관의 역할)**와 **실제로 부품을 끼우는 방법(작업자의 역할)**을 분리해서, 코드를 나중에도 아주 유연하게 고치고 재사용할 수 있도록 만든 똑똑한 설계 방식이랍니다.