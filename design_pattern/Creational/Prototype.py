# copy 모듈은 파이썬에서 기본으로 제공하는 '복사기' 같은 도구 모음집이에요.
import copy

# Prototype(원본) 클래스: 복사기에 들어갈 수 있는 물건들의 '기본 설계도'입니다.
class Prototype:
    # 물건이 처음 만들어질 때, 어떤 값(value)을 가질지 정해줍니다.
    def __init__(self, value):
        self.value = value

    # clone(복제) 기능: 이 패턴의 핵심인 '복사 버튼'입니다!
    def clone(self):
        # copy.deepcopy()는 겉모습뿐만 아니라 속까지 완전히 똑같은 '완벽한 복제품'을 만들어주는 기능이에요.
        # self(자기 자신)를 복사기에 넣고 돌려서 나온 새로운 결과물을 돌려줍니다.
        return copy.deepcopy(self)


# ---------------------------------------------------------
# 실제 복사기 사용해보기!
# ---------------------------------------------------------

# 1. 먼저 원본 물건(레고 성)을 하나 만듭니다. 이 물건의 가치는 '10'이라고 해볼게요.
# (이 과정이 원래는 엄청 오래 걸리고 힘든 작업이라고 상상해 보세요!)
prototype1 = Prototype(10)

# 2. 똑같은 물건이 하나 더 필요하네요? 
# 처음부터 새로 만들지 않고, 원본 물건의 '복사 버튼(clone)'을 눌러서 복제품을 만듭니다.
prototype2 = prototype1.clone()

# 3. 원본(prototype1)과 복제품(prototype2)의 값을 확인해 볼까요?
print(prototype1.value, prototype2.value)  # 결과: 10 10 (둘 다 똑같이 10입니다!)

# 4. 이제 복사해서 얻은 복제품(prototype2)을 살짝 개조해 봅니다. 가치를 '20'으로 바꿨어요.
prototype2.value = 20

# 5. 다시 두 물건의 값을 확인해 봅니다.
# 결과: 10 20
# 복제품만 20으로 바뀌었고, 원본은 안전하게 10을 유지하고 있죠?
# 즉, 둘은 처음엔 똑같았지만 일란성 쌍둥이처럼 연결되어 있지 않은 '완전히 독립된 별개의 물건'이라는 뜻이에요!
print(prototype1.value, prototype2.value)