# ---------------------------------------------------------
# 1. Target (우리가 맞춰야 할 '새로운 표준 규격')
# ---------------------------------------------------------
# 예를 들어, 현재 호텔 방에 있는 '납작한 모양의 콘센트'라고 생각해 보세요.
class Target:
    def request(self):
        # 이 콘센트는 무조건 "납작한 모양으로 꽂아라(request)"라는 규칙이 있습니다.
        pass

# ---------------------------------------------------------
# 2. Adaptee (내가 이미 가지고 있는, '규격이 안 맞는 옛날 제품')
# ---------------------------------------------------------
# 예를 들어, 내가 한국에서 챙겨 온 '동그란 모양의 노트북 충전기'입니다.
class Adaptee:
    # 이 충전기는 콘센트의 규칙(request)을 모르고, 자기만의 방식(specific_request)으로만 작동해요.
    def specific_request(self):
        print("Specific request") # "동그란 플러그로 전기를 받아 노트북을 충전합니다!"

# ---------------------------------------------------------
# 3. Adapter (어댑터 / 일명 '돼지코')
# ---------------------------------------------------------
# Target(납작한 콘센트)을 상속받아서, 겉모습은 납작한 콘센트에 딱 맞게 만듭니다.
class Adapter(Target):
    
    # 어댑터를 살 때, 어떤 제품(adaptee)에 끼울 건지 정합니다.
    def __init__(self, adaptee):
        # 어댑터 뒤쪽 구멍에 동그란 충전기(adaptee)를 쏙 끼워 품고 있습니다.
        self.adaptee = adaptee

    # 호텔 콘센트(Target)가 어댑터에게 "전기 줄게, 작동해!(request)"라고 명령하면...
    def request(self):
        # 어댑터가 중간에서 전기를 변환한 뒤, 
        # 자기가 품고 있는 충전기의 진짜 작동 버튼(specific_request)을 대신 눌러줍니다!
        self.adaptee.specific_request()

# ---------------------------------------------------------
# 4. 실제 어댑터 사용해보기!
# ---------------------------------------------------------

# 1. 짐 가방에서 규격이 안 맞는 '동그란 노트북 충전기(Adaptee)'를 꺼냅니다.
adaptee = Adaptee()

# 2. 이 충전기를 호텔 콘센트에 꽂기 위해 '돼지코(Adapter)'를 하나 꺼내서 충전기에 끼워줍니다.
adapter = Adapter(adaptee)

# 3. 이제 돼지코가 끼워진 충전기를 호텔의 납작한 콘센트에 꽂고 작동(request)시킵니다!
# 결과: 돼지코가 중간에서 통역을 해주어 충전기(Specific request)가 무사히 작동합니다.
adapter.request()

# 💡 머리에 쏙 들어오는 핵심 요약
# 코드를 뜯어고치지 않는다: 옛날 코드(Adaptee)를 새로운 시스템(Target)에 맞게 바꾸고 싶을 때, 
# 옛날 코드를 직접 수정하는 것은 위험하고 복잡할 수 있습니다.

# 중간 통역사: 대신 Adapter라는 껍데기(중간 통역사)를 하나 만들어서, 
# 겉으로는 새로운 시스템의 규칙을 따르는 척하면서 속으로는 옛날 코드를 몰래 실행시켜 주는 것입니다.

