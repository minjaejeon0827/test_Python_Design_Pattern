# 구조 패턴
# 구조 패턴(Structural Patterns)은 클래스나 객체를 조합하여 더 큰 구조를 형성하고, 
# 새로운 기능을 추가하거나 기존 기능을 수정하는 데 도움을 줍니다. 
# 구조 패턴을 통해 객체 간의 관계를 유연하게 정의하고, 
# 시스템의 구조를 명확하게 설계할 수 있습니다. 
# 이 섹션에서는 다양한 구조 패턴을 소개하겠습니다.

# 어댑터 패턴 (Adapter Pattern)
# 어댑터 패턴은 호환되지 않는 인터페이스를 가진 클래스들을 연결해주는 패턴입니다. 
# 이를 통해 기존 클래스를 수정하지 않고도 서로 다른 인터페이스를 가진 객체들 간의 협업을 가능하게 합니다.
# 참고: https://wikidocs.net/252293

