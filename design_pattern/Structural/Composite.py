# 가장 중요한 핵심은, 컴퓨터 입장에서는 "파일 하나를 복사하는 것"이나 "파일 100개가 든 폴더 하나를 복사하는 것"이나 똑같이 '복사하기'라는 하나의 명령으로 처리할 수 있다는 점입니다.

# 단일 객체(파일)와 복합 객체(폴더)를 차별하지 않고 '똑같은 녀석'으로 취급해서 다루기 쉽게 만들어 주는 마법, 그것이 바로 컴포지트 패턴입니다!

# 폴더와 파일의 비유를 생각하며, 한 줄 한 줄 주석을 따라가 볼까요?

# 💡 머리에 쏙 들어오는 핵심 요약
# 부분과 전체를 똑같이 다루기: 이 패턴을 쓰면 코드를 사용하는 사람 입장에서는 지금 내가 누른 게 '단일 파일(Leaf)'인지 엄청나게 큰 '폴더(Composite)'인지 굳이 검사할 필요가 없습니다. 그냥 .operation()(실행해!) 한 번만 외치면 알아서 작동하니까요.

# 트리 구조(Tree Structure): 폴더 안에 폴더, 그 안에 또 폴더, 그 안에 파일... 이렇게 나무가 가지를 뻗어나가듯 꼬리에 꼬리를 무는 계층 구조를 프로그래밍으로 아주 깔끔하게 구현할 수 있는 방법입니다.

# 어디에 많이 쓰이나요? 파일 시스템, 회사의 조직도(사장-부장-팀원), 그래픽 프로그램에서 여러 도형을 그룹으로 묶어서 한 번에 색깔을 바꾸거나 이동시킬 때 아주 유용하게 쓰인답니다!

# ---------------------------------------------------------
# 1. Component (모든 항목이 지켜야 할 '기본 틀')
# ---------------------------------------------------------
# 파일이든 폴더든, 컴퓨터 안에서는 모두 하나의 '항목'으로 취급합니다.
class Component:
    def operation(self):
        # 모든 항목은 '실행하기(operation)' 기능이 무조건 있어야 합니다.
        pass

# ---------------------------------------------------------
# 2. Leaf (더 이상 쪼개지지 않는 단일 객체 = '파일')
# ---------------------------------------------------------
# 사진, 문서, 음악 같은 개별 파일입니다.
class Leaf(Component):
    def operation(self):
        print("Leaf operation") # "파일이 개별적으로 실행되었습니다!"

# ---------------------------------------------------------
# 3. Composite (안에 다른 것을 담는 복합 객체 = '폴더')
# ---------------------------------------------------------
# 파일이나 또 다른 하위 폴더를 담을 수 있는 '상자'입니다.
class Composite(Component):
    
    def __init__(self):
        # 폴더를 처음 만들면, 내용물을 담아둘 빈 공간(리스트)을 준비합니다.
        self._children = []

    def add(self, component):
        # 폴더 안에 새로운 항목(파일이나 하위 폴더)을 '넣습니다'.
        self._children.append(component)

    def remove(self, component):
        # 폴더 안에 있던 항목(파일이나 하위 폴더)을 '뺍니다'.
        self._children.remove(component)

    def operation(self):
        # 폴더 자체를 실행하면 어떻게 될까요?
        print("Composite operation") # "폴더를 엽니다. 이제 안에 있는 것들을 모두 실행할게요!"
        
        # 폴더 안에 들어있는 모든 항목(_children)들을 하나씩 차례대로 꺼내서...
        for child in self._children:
            # 각각의 항목들을 자동으로 실행(operation) 시켜 줍니다!
            child.operation()


# ---------------------------------------------------------
# 4. 실제 폴더와 파일 만들어보기!
# ---------------------------------------------------------

# 1. 나뭇잎 2개(단일 파일 2개)를 만듭니다. 
leaf1 = Leaf() # 예: 가족사진.jpg
leaf2 = Leaf() # 예: 일기장.txt

# 2. 이 파일들을 담을 빈 폴더(Composite)를 하나 만듭니다.
composite = Composite() # 예: '내 문서' 폴더

# 3. 빈 폴더 안에 아까 만든 파일 2개를 차례대로 쏙쏙 집어넣습니다.
composite.add(leaf1)
composite.add(leaf2)

# 4. 자, 이제 '내 문서' 폴더 하나만 콕 집어서 "실행해!"라고 명령을 내립니다.
# 결과: 폴더가 열리면서 안에 들어있던 가족사진과 일기장이 한꺼번에 자동으로 실행됩니다!
composite.operation()