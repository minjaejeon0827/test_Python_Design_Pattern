# 이번에 만나볼 패턴은 '데코레이터 패턴(Decorator Pattern)'입니다! 이름에서 알 수 있듯이 무언가를 '장식(Decorate)'하는 역할을 해요.

# 이해하기 쉽게 '나만의 맞춤형 케이크 만들기'를 상상해 볼까요?
# 처음에는 아무것도 없는 '기본 스펀지 케이크'로 시작합니다. 여기에 '딸기'를 얹고 싶으면 딸기 장식을 입히고, 그 위에 '초코 시럽'을 뿌리고 싶으면 초코 시럽 장식을 한 겹 더 입히는 거예요.

# 만약 데코레이터 패턴을 쓰지 않는다면, '딸기 케이크', '초코 케이크', '딸기초코 케이크' 등등 모든 조합의 케이크를 처음부터 각각 다 만들어야 해서 엄청나게 복잡해질 거예요. 하지만 이 패턴을 쓰면 '기본 케이크'에 원하는 '토핑 장식'들만 쏙쏙 겹쳐서 씌우면 되니까 아주 편리하답니다!

# 머릿속에 맛있는 케이크를 그리며, 주석을 한 줄씩 천천히 따라가 볼까요?

# 💡 머리에 쏙 들어오는 핵심 요약
# 자유로운 확장(토핑 추가): 이미 잘 만들어진 객체(기본 빵)를 수정하지 않고도, 겉에 새로운 객체(토핑)를 겹겹이 씌우는 것만으로 기능을 무한히 추가할 수 있습니다.

# 러시아 인형(마트료시카) 구조: 인형을 열면 더 작은 인형이 계속 나오듯, 초코 장식을 실행하면 그 안의 딸기 장식이 실행되고, 그 안에 있는 기본 빵이 실행되는 방식으로 연결되어 작동합니다.

# 조합의 마법: 코드를 작성할 때 '기본 빵', '딸기', '초코' 세 가지만 만들어 두면, 이들을 조립해서 수십 가지의 다양한 결과를 만들어낼 수 있는 아주 마법 같은 설계 방식이랍니다!

# ---------------------------------------------------------
# 1. Component (모든 케이크와 토핑들의 '기본 틀')
# ---------------------------------------------------------
# 케이크든 토핑이든, 결국 다 합쳐지면 '하나의 케이크'처럼 취급되어야 해요.
class Component:
    def operation(self):
        # 모든 케이크 요소들은 '작동(맛보기)' 기능이 있어야 합니다.
        pass

# ---------------------------------------------------------
# 2. ConcreteComponent (토핑이 하나도 없는 '기본 스펀지 케이크')
# ---------------------------------------------------------
# 장식이 올라갈 가장 안쪽의 진짜 빵입니다.
class ConcreteComponent(Component):
    def operation(self):
        print("ConcreteComponent operation") # "가장 안쪽에는 기본 스펀지 케이크가 있습니다!"


# ---------------------------------------------------------
# 3. Decorator (장식, 토핑들의 '기본 틀')
# ---------------------------------------------------------
# 장식은 반드시 '어떤 케이크 위에 올라갈지' 알아야 합니다.
class Decorator(Component):
    
    def __init__(self, component):
        # 자기가 감쌀 대상(알맹이가 되는 케이크나 또 다른 장식)을 속에 품고(기억하고) 있습니다.
        self._component = component

    def operation(self):
        # 장식 자체를 작동시키면, 기본적으로 자기가 품고 있는 안쪽 알맹이의 작동을 그대로 부릅니다.
        self._component.operation()


# ---------------------------------------------------------
# 4. ConcreteDecorator A, B (진짜 토핑들)
# ---------------------------------------------------------

# 딸기 토핑 (장식 A)
class ConcreteDecoratorA(Decorator):
    def operation(self):
        # 1. 자기 자신이 할 일(장식)을 먼저 뽐냅니다.
        print("ConcreteDecoratorA operation") # "딸기 토핑이 추가되었습니다!"
        
        # 2. 그리고 자기가 감싸고 있던 안쪽 알맹이의 기능을 이어서 실행합니다.
        super().operation() 

# 초코 시럽 토핑 (장식 B)
class ConcreteDecoratorB(Decorator):
    def operation(self):
        # 1. 자기 자신이 할 일(장식)을 먼저 뽐냅니다.
        print("ConcreteDecoratorB operation") # "초코 시럽이 듬뿍 뿌려졌습니다!"
        
        # 2. 그리고 자기가 감싸고 있던 안쪽 알맹이의 기능을 이어서 실행합니다.
        super().operation()


# ---------------------------------------------------------
# 5. 실제 케이크에 장식 올려보기!
# ---------------------------------------------------------

# 1. 가장 먼저 뼈대가 될 '기본 스펀지 케이크'를 하나 굽습니다.
component = ConcreteComponent()

# 2. 그 기본 케이크 겉에 '딸기 토핑(Decorator A)'을 한 겹 씌웁니다.
# 이제 decorator_a는 기본 케이크를 품은 딸기 케이크가 되었습니다.
decorator_a = ConcreteDecoratorA(component)

# 3. 딸기 케이크 겉에 이번에는 '초코 시럽(Decorator B)'을 한 겹 더 씌웁니다.
# 이제 decorator_b는 (기본 케이크 + 딸기)를 품은 초코 케이크가 되었습니다.
decorator_b = ConcreteDecoratorB(decorator_a)

# 4. 완성된 최종 케이크를 맛봅니다(실행)!
# 가장 겉에 있는 초코 시럽(B)부터 시작해서, 그 안의 딸기(A), 가장 안쪽의 빵 순서로 실행됩니다.
decorator_b.operation()