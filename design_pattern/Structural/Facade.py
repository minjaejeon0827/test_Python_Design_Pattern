# '퍼사드(Facade)'는 프랑스어로 '건물의 정면(외관)'이라는 뜻이에요. 건물 밖에서 보면 깔끔하고 예쁜 출입문 하나만 보이지만, 사실 건물 안에는 아주 복잡한 배관, 전선, 기계실이 잔뜩 얽혀 있죠?

# 이 패턴을 가장 쉽게 이해하려면 스마트홈의 '영화 보기 모드' 버튼을 떠올려 보세요.
# 우리가 영화를 볼 때 1) 조명을 어둡게 끄고, 2) 커튼을 치고, 3) 빔프로젝터를 켜고, 4) 스피커 볼륨을 높여야 합니다. 이 복잡한 과정(서브시스템)을 하나하나 직접 챙기려면 너무 귀찮겠죠?
# 이때 "영화 보기 버튼(퍼사드)"을 하나 딱 만들어 두면, 우리는 그 버튼 하나만 눌러도 뒤에서 알아서 저 4가지 복잡한 작업들이 순서대로 척척 진행됩니다.

# 즉, 사용자가 쓰기 편하게 복잡한 기능들을 다 모아서 '단순한 앞문(버튼)' 하나만 뚫어주는 것이 바로 퍼사드 패턴입니다.

# 자, 그럼 이 똑똑한 '단축 버튼'이 어떻게 만들어지는지 주석을 통해 한 줄씩 살펴볼까요?

# 💡 머리에 쏙 들어오는 핵심 요약
# 복잡함 숨기기: 기계들이 어떻게 생겼고, 어떤 순서로 켜야 하는지 등 복잡하고 머리 아픈 내부 사정은 Facade 클래스 안쪽에 전부 숨겨버립니다.

# 사용자의 엄청난 편리함: 코드를 사용하는 사람(고객)은 Subsystem A나 B를 따로따로 불러올 필요 없이, 오직 Facade라는 깔끔한 문(인터페이스) 하나만 열면 모든 것을 해결할 수 있습니다.

# 안전한 격리: 나중에 '빔프로젝터(B)'를 최신형으로 바꾸더라도, 사용자는 여전히 똑같은 Facade의 operation() 버튼만 누르면 됩니다. 즉, 내부 기계가 바뀌어도 사용자는 아무런 영향을 받지 않고 편하게 쓸 수 있는 것이죠!

# ---------------------------------------------------------
# 1. 서브시스템(Subsystem): 복잡하고 다양한 '개별 기계들'
# ---------------------------------------------------------

# 첫 번째 기계 (예: 스마트 조명 시스템)
class SubsystemA:
    def operation_a(self):
        # 조명만의 복잡한 작동 방식이 있다고 상상해 보세요.
        print("SubsystemA operation") # "조명을 영화 보기에 딱 좋은 밝기로 어둡게 조절합니다."

# 두 번째 기계 (예: 빔프로젝터 시스템)
class SubsystemB:
    def operation_b(self):
        # 프로젝터만의 복잡한 작동 방식이 있습니다.
        print("SubsystemB operation") # "빔프로젝터 전원을 켜고 화면을 벽에 맞춥니다."


# ---------------------------------------------------------
# 2. 퍼사드(Facade): 사용자를 위한 '단순한 앞문(영화 보기 버튼)'
# ---------------------------------------------------------

class Facade:
    # '영화 보기 버튼'을 처음 만들 때, 어떤 기계들과 연결해 둘지 미리 싹 다 세팅해 둡니다.
    def __init__(self):
        # 퍼사드 내부에 조명(A)과 프로젝터(B)를 준비해 둡니다.
        self._subsystem_a = SubsystemA()
        self._subsystem_b = SubsystemB()

    # 사용자가 실제로 누르게 될 딱 하나의 단순한 '작동 버튼'입니다.
    def operation(self):
        # 사용자는 이 버튼 하나만 눌렀지만, 
        # 내부에서는 퍼사드가 알아서 조명(A)도 조절하고, 프로젝터(B)도 켜줍니다!
        self._subsystem_a.operation_a()
        self._subsystem_b.operation_b()


# ---------------------------------------------------------
# 3. 실제 퍼사드(단축 버튼) 사용해보기!
# ---------------------------------------------------------

# 1. 복잡한 기계들을 하나로 묶어둔 '단축 버튼(Facade)'을 하나 가져옵니다.
# (사용자는 조명이니 프로젝터니 하는 복잡한 존재들을 아예 몰라도 됩니다.)
facade = Facade()

# 2. 단축 버튼을 꾹 누릅니다! (실행)
facade.operation()  

# 실행 결과:
# "SubsystemA operation" (조명이 켜지고)
# "SubsystemB operation" (이어서 프로젝터가 켜집니다!)