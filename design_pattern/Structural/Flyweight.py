# 드디어 구조 패턴의 마지막인 '플라이웨이트 패턴(Flyweight Pattern)'까지 오셨네요! 복잡한 개념들을 하나하나 정복해 나가시는 모습이 정말 훌륭합니다.

# '플라이웨이트(Flyweight)'는 권투의 체급 중에서 아주 가벼운 체급을 뜻하는 말이에요. 이 패턴의 핵심 목표도 바로 "프로그램을 아주 가볍게(메모리를 적게 쓰도록) 만드는 것"입니다.

# 이해하기 쉽게 '마인크래프트 같은 블록 게임 만들기'를 상상해 볼까요?
# 게임 맵에 수만 개의 '참나무 블록'을 깔아야 합니다. 만약 참나무 블록 수만 개가 각각 자신만의 '3D 그래픽 이미지(무겁고 변하지 않는 정보)'를 따로따로 메모리에 들고 있다면 컴퓨터가 뻗어버릴 거예요.

# 그래서 '참나무 3D 그래픽(내부 상태)'은 딱 하나만 만들어서 모두가 공유하고, 각 블록들이 맵의 어느 위치에 놓일지(외부 상태)만 따로따로 전달해 주는 방식이 바로 플라이웨이트 패턴입니다! 무거운 짐은 창고에 하나만 두고, 가벼운 쪽지만 들고 다니는 셈이죠.

# 블록 게임의 원리를 생각하며, 주석을 한 줄씩 천천히 따라가 볼까요?

# 💡 머리에 쏙 들어오는 핵심 요약
# 반으로 쪼개기: 객체가 가진 정보들을 "절대 안 변하고 무거운 것(Intrinsic)"과 "그때그때 변하고 가벼운 것(Extrinsic)"으로 분리하는 것이 시작입니다.

# 공유의 마법: 무거운 정보는 팩토리(창고)에 종류별로 딱 하나씩만 만들어두고, 필요할 때마다 꺼내서 돌려씁니다. (전에 배운 싱글톤 패턴과 비슷하지만, 이건 딕셔너리를 통해 '여러 종류'를 하나씩 챙겨둔다는 점이 다릅니다.)

# 메모리 다이어트: 수천, 수만 개의 객체를 만들어야 하는 상황(예: 게임의 총알, 숲속의 나무, 문서 작성 프로그램의 글꼴 등)에서 컴퓨터의 과부하를 막아주는 아주 훌륭하고 필수적인 테크닉입니다.

# ---------------------------------------------------------
# 1. Flyweight (공유할 객체, '기본 참나무 블록 모델')
# ---------------------------------------------------------
class Flyweight:
    def __init__(self, intrinsic_state):
        # intrinsic(고유한, 변하지 않는 내부) 상태를 저장합니다.
        # 예: "이 블록은 참나무 3D 텍스처를 가짐" (메모리를 많이 차지하는 무거운 데이터)
        self._intrinsic_state = intrinsic_state

    def operation(self, extrinsic_state):
        # extrinsic(외부의, 그때그때 변하는) 상태를 외부에서 따로 전달받아서 작동합니다.
        # 예: "우리가 공유하는 참나무 그래픽(내부)을 x:10, y:20 좌표(외부)에 그려라!"
        print(f"Intrinsic state: {self._intrinsic_state}, Extrinsic state: {extrinsic_state}")


# ---------------------------------------------------------
# 2. FlyweightFactory (공유 객체들을 관리하고 빌려주는 '창고')
# ---------------------------------------------------------
class FlyweightFactory:
    # 지금까지 만들어둔 블록 모델들을 보관할 빈 보관함(딕셔너리)을 준비합니다.
    _flyweights = {}

    # @staticmethod는 이 함수를 공용으로 쓰겠다는 파이썬의 기능입니다.
    @staticmethod
    def get_flyweight(key):
        # 누군가 특정 키값(예: "참나무")의 객체를 요구하면, 먼저 창고에 있는지 확인합니다.
        if key not in FlyweightFactory._flyweights:
            # 만약 창고에 없으면? 그때 딱 하나 새로 만들어서 창고에 저장해 둡니다.
            FlyweightFactory._flyweights[key] = Flyweight(key)
        
        # 창고에 이미 있거나 방금 새로 만들었으니, 그 객체를 빌려줍니다.
        return FlyweightFactory._flyweights[key]


# ---------------------------------------------------------
# 3. 실제 게임에서 블록 배치해보기!
# ---------------------------------------------------------

# 1. 창고에 "state1"(참나무) 블록을 달라고 요청합니다. (처음이라 새로 만들어짐)
flyweight1 = FlyweightFactory.get_flyweight("state1")
# 꺼내온 객체에 "extrinsic1"(x:10, y:20 위치)라는 외부 상태를 던져주면서 화면에 그립니다.
flyweight1.operation("extrinsic1")

# 2. 맵의 다른 곳에 참나무를 심기 위해 또 창고에 "state1"을 달라고 요청합니다.
# (이번엔 이미 창고에 만들어둔 게 있으니, 새로 만들지 않고 기존 것을 줍니다!)
flyweight2 = FlyweightFactory.get_flyweight("state1")
# 꺼내온 객체에 "extrinsic2"(x:50, y:100 위치)라는 새로운 외부 위치 정보를 던져주며 그립니다.
flyweight2.operation("extrinsic2")

# 3. 이 두 변수가 가리키는 대상이 정말로 '같은 객체'일까요?
# 결과: True! 둘은 이름만 다를 뿐, 메모리상의 완벽히 똑같은 '하나의 참나무 모델'을 공유하고 있습니다.
print(flyweight1 is flyweight2)  # True