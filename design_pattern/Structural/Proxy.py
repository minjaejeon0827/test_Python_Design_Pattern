# 이번에 만나볼 패턴은 '프록시 패턴(Proxy Pattern)'이네요! 구조 패턴들을 하나씩 정복해 나가시는 모습이 정말 멋집니다.

# '프록시(Proxy)'라는 말은 우리말로 '대리인'이라는 뜻이에요. 이 패턴을 가장 쉽게 이해하려면 '유명 연예인과 매니저'의 관계를 떠올려 보시면 됩니다.

# 만약 우리가 유명한 아이돌 가수(진짜 객체)를 학교 축제에 섭외하고 싶다고 해볼까요? 가수에게 직접 전화를 걸 수는 없겠죠? 대신 가수의 매니저(프록시)에게 연락을 해야 합니다. 매니저는 가수를 대신해서 전화를 받고, 스케줄이 비어 있는지 확인한 다음, 모든 조건이 맞을 때만 진짜 가수에게 "이날 공연이 있습니다"라고 전달해 주죠.

# 이처럼 프로그램 안에서도 무겁고 중요한 '진짜 객체'를 직접 부르지 않고, 가벼운 '대리인 객체'를 찰떡같이 세워두어 일을 처리하는 아주 똑똑한 방법이랍니다.

# 연예인과 매니저를 생각하며 주석이 달린 코드를 한 줄씩 살펴볼까요?

# 💡 머리에 쏙 들어오는 핵심 요약
# 왜 굳이 대리인을 세우나요? 가수(진짜 객체)가 너무 바쁘거나, 준비하는 데 시간이 오래 걸리거나, 혹은 아무나 접근하면 안 되는 중요한 보안 정보일 때가 있어요. 이때 매니저(프록시)가 앞에서 든든하게 문지기 역할을 해주면 프로그램이 훨씬 안전하고 빠르게 돌아갈 수 있답니다.

# 접근 제어 (Access Control): 매니저는 스케줄이 꽉 찼으면 가수에게 묻지도 않고 "거절"할 수 있는 권한이 있습니다. 즉, 진짜 객체에 무리한 요청이 가지 않도록 중간에서 막아줄 수 있어요.

# 사용자는 똑같이 느낍니다: 코드를 실행하는 사람 입장에서는 매니저(proxy)의 request()를 부르든 가수(real_subject)의 request()를 부르든 똑같은 명령어를 사용합니다. 즉, 사용자는 뒤에서 대리인이 일하는지 진짜가 일하는지 신경 쓰지 않고 편하게 요청만 하면 된답니다.

# ---------------------------------------------------------
# 1. Subject (모두가 지켜야 할 '공통 규칙')
# ---------------------------------------------------------
# 가수와 매니저 모두 겉으로는 "공연 요청을 처리할 수 있다"는 공통점이 있어야 해요.
class Subject:
    def request(self):
        # "요청(request)이 들어오면 응답해야 한다"는 기본 규칙입니다.
        pass

# ---------------------------------------------------------
# 2. RealSubject (진짜 연예인)
# ---------------------------------------------------------
# 우리가 진짜로 일을 시키고 싶은 '아이돌 가수'입니다.
class RealSubject(Subject):
    def request(self):
        # 무대에 올라가서 진짜로 노래를 부르는 핵심적인 일을 합니다.
        print("RealSubject request") # "진짜 객체(가수)가 요청을 받아 노래합니다!"

# ---------------------------------------------------------
# 3. Proxy (대리인, 매니저)
# ---------------------------------------------------------
# 진짜 연예인을 대신해서 외부의 연락을 먼저 받는 '매니저'입니다.
class Proxy(Subject):
    
    def __init__(self, real_subject):
        # 매니저는 자기가 담당하는 '진짜 연예인(real_subject)'의 연락처를 기억하고 있어야 해요.
        self._real_subject = real_subject

    def request(self):
        # 외부에서 섭외 요청이 들어오면 매니저가 먼저 전화를 받습니다.
        print("Proxy handling request") # "매니저가 먼저 요청을 확인하고 일정을 조율합니다."
        
        # 매니저 선에서 준비가 끝나면, 자기가 담당하는 진짜 연예인에게 무대에 오르라고 전달합니다.
        self._real_subject.request()


# ---------------------------------------------------------
# 4. 실제 매니저를 통해 일 시켜보기!
# ---------------------------------------------------------

# 1. 기획사에서 '진짜 아이돌 가수(RealSubject)'를 한 명 발탁합니다.
real_subject = RealSubject()

# 2. 이 가수를 전담해서 보호하고 연락을 대신 받을 '매니저(Proxy)'를 고용합니다.
# 매니저에게 담당할 가수를 연결해 줍니다.
proxy = Proxy(real_subject)

# 3. 드디어 학교 축제에서 섭외 요청(request)을 보냅니다!
# 주의: 우리는 진짜 가수에게 연락한 게 아니라, 매니저(proxy)에게 연락한 거예요.
proxy.request()  

# 실행 결과:
# 1. "Proxy handling request" (매니저가 먼저 전화를 받아서 확인하고)
# 2. "RealSubject request" (진짜 가수가 무대에 올라가 노래합니다)